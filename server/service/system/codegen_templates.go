func resolveFrontendRoot(value string) string {
	value = defaultString(value, "web")
	if filepath.IsAbs(value) {
		return value
	}
	candidates := []string{
		filepath.Clean(value),
		filepath.Join("..", value),
		filepath.Join("..", "..", value),
	}
	for _, candidate := range candidates {
		if info, err := os.Stat(candidate); err == nil && info.IsDir() {
			return candidate
		}
	}
	return value
}

func toPascalCase(value string) string {
	parts := regexp.MustCompile(`[_\-\s]+`).Split(value, -1)
	var out strings.Builder
	for _, part := range parts {
		if part == "" {
			continue
		}
		out.WriteString(strings.ToUpper(part[:1]))
		if len(part) > 1 {
			out.WriteString(strings.ToLower(part[1:]))
		}
	}
	return out.String()
}

func toCamelCase(value string) string {
	pascal := toPascalCase(value)
	if pascal == "" {
		return ""
	}
	return strings.ToLower(pascal[:1]) + pascal[1:]
}

func toKebabCase(value string) string {
	value = regexp.MustCompile(`([a-z0-9])([A-Z])`).ReplaceAllString(value, "$1-$2")
	value = strings.ReplaceAll(value, "_", "-")
	value = strings.ReplaceAll(value, " ", "-")
	value = regexp.MustCompile(`-+`).ReplaceAllString(value, "-")
	return strings.Trim(strings.ToLower(value), "-")
}

func syncCodegenMenus(ctx codegenContext) ([]uint, error) {
	parentID := uint(0)
	if ctx.Table.BelongMenuID != nil {
		parentID = *ctx.Table.BelongMenuID
	}
	page, err := upsertCodegenMenu(parentID, defaultString(stringValue(ctx.Table.MenuName), ctx.ClassName), ctx.RoutePath, "/"+ctx.RoutePath, ctx.PackageName+"/"+ctx.BusinessName+"/index", "M", 10)
	if err != nil {
		return nil, err
	}
	ids := []uint{page.ID}
	actions := []struct {
		name string
		code string
		sort uint16
	}{
		{"列表", ctx.RoutePath + "/index", 10},
		{"新增", ctx.RoutePath + "/create", 20},
		{"编辑", ctx.RoutePath + "/update", 30},
		{"删除", ctx.RoutePath + "/destroy", 40},
	}
	for _, action := range actions {
		item, err := upsertCodegenMenu(page.ID, action.name, action.code, "", "", "B", action.sort)
		if err != nil {
			return ids, err
		}
		ids = append(ids, item.ID)
	}
	return ids, nil
}

func upsertCodegenMenu(parentID uint, name, code, route, component, typ string, sortValue uint16) (systemModel.AISystemMenu, error) {
	db, err := systemDB()
	if err != nil {
		return systemModel.AISystemMenu{}, err
	}
	var existing systemModel.AISystemMenu
	err = db.Where("code = ? AND delete_time IS NULL", code).First(&existing).Error
	payload := map[string]interface{}{
		"parent_id":   parentID,
		"name":        name,
		"code":        code,
		"route":       route,
		"component":   component,
		"type":        typ,
		"icon":        "AppstoreOutlined",
		"sort":        sortValue,
		"is_hidden":   2,
		"is_layout":   1,
		"status":      1,
		"update_time": gorm.Expr("NOW()"),
	}
	if typ == "B" {
		payload["is_hidden"] = 1
		payload["icon"] = ""
	}
	if errorsIsRecordNotFound(err) {
		menu := systemModel.AISystemMenu{}
		payload["create_time"] = gorm.Expr("NOW()")
		if err := db.Table("ai_system_menu").Create(payload).Error; err != nil {
			return menu, err
		}
		err = db.Where("code = ? AND delete_time IS NULL", code).First(&menu).Error
		return menu, err
	}
	if err != nil {
		return existing, err
	}
	err = db.Model(&systemModel.AISystemMenu{}).Where("id = ?", existing.ID).Updates(payload).Error
	if err != nil {
		return existing, err
	}
	err = db.Where("id = ?", existing.ID).First(&existing).Error
	return existing, err
}

func errorsIsRecordNotFound(err error) bool {
	return errors.Is(err, gorm.ErrRecordNotFound)
}
